import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";

const root = process.cwd();
execFileSync(process.execPath, [path.join(root, "scripts/prepare-terminal4-runtime.mjs")], { stdio: "inherit" });

const generatedPath = path.join(root, "src/components/RampReadyStandupTrainerTerminal4.jsx");
const entryPath = path.join(root, "src/components/PushbackTrainer.jsx");
const playwrightPath = path.join(root, "playwright.config.js");
const generated = fs.readFileSync(generatedPath, "utf8");
const entry = fs.readFileSync(entryPath, "utf8");
const playwright = fs.readFileSync(playwrightPath, "utf8");
const failures = [];

const requireText = (text, label) => {
  if (!generated.includes(text)) failures.push(`generated trainer missing ${label}`);
};

requireText('buildTerminal4RampEnvironment', "Terminal 4 builder import");
requireText('const environment = buildGround(scene);', "live scene environment construction");
requireText('camera, environment, rig, aircraft', "environment retention in simulator state");
requireText('Terminal4RampEnvironment', "environment runtime group");

if (generated.includes("new THREE.PlaneGeometry(90, 140)")) failures.push("legacy 90 x 140 flat ramp remains in active generated trainer");
if (!entry.includes('import RampReadyStandupTrainer from "./RampReadyStandupTrainerTerminal4.jsx";')) failures.push("PushbackTrainer does not route to prepared Terminal 4 trainer");
if (!generated.startsWith("// GENERATED by scripts/prepare-terminal4-runtime.mjs")) failures.push("generated trainer banner missing");
if (!playwright.includes('const terminal4Preparation = "npm run prepare:terminal4-runtime";')) failures.push("Playwright browser verification does not declare Terminal 4 runtime preparation");
if (!playwright.includes('`${terminal4Preparation} && ${requestedWebServerCommand}`')) failures.push("Playwright browser server can launch without generating the active Terminal 4 trainer");

const first = fs.readFileSync(generatedPath, "utf8");
execFileSync(process.execPath, [path.join(root, "scripts/prepare-terminal4-runtime.mjs")], { stdio: "inherit" });
const second = fs.readFileSync(generatedPath, "utf8");
if (first !== second) failures.push("Terminal 4 runtime preparation is not deterministic");

if (failures.length) {
  console.error("RampReady Terminal 4 runtime integration verification failed:");
  failures.forEach((failure) => console.error(`- ${failure}`));
  process.exit(1);
}

console.log("RampReady Terminal 4 runtime integration verified: live and browser trainers prepare the 180 x 260 m environment, legacy flat ground is absent, and generation is deterministic.");
