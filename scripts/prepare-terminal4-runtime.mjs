import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const sourcePath = path.join(root, "src/components/RampReadyTrainerStable.jsx");
const outputPath = path.join(root, "src/components/RampReadyTrainerTerminal4.jsx");

const source = fs.readFileSync(sourcePath, "utf8");
const importAnchor = 'import { buildCRJ700Aircraft } from "./aircraft/crj700Model.js";';
const environmentImport = 'import { buildTerminal4RampEnvironment } from "../environment/terminal4RampEnvironment.js";';
const groundStart = source.indexOf("function buildGround(scene) {");
const groundEndMarker = "\n\nfunction buildTug()";
const groundEnd = source.indexOf(groundEndMarker, groundStart);

if (!source.includes(importAnchor)) throw new Error("CRJ700 import anchor not found in trainer source");
if (groundStart < 0 || groundEnd < 0) throw new Error("Legacy buildGround block not found in trainer source");
if (!source.includes("    buildGround(scene);")) throw new Error("Legacy buildGround call not found in trainer source");

const replacementGround = `function buildGround(scene) {
  const environment = buildTerminal4RampEnvironment(THREE);
  environment.position.set(0, 0, 0);
  scene.add(environment);
  return environment;
}`;

let prepared = source
  .replace(importAnchor, `${importAnchor}\n${environmentImport}`)
  .slice(0, groundStart)
  + replacementGround
  + source.slice(groundEnd);

prepared = prepared
  .replace("    buildGround(scene);", "    const environment = buildGround(scene);")
  .replace(
    "const sim = { scene, renderer, camera, tug, wheels, aircraft,",
    "const sim = { scene, renderer, camera, environment, tug, wheels, aircraft,",
  );

if (!prepared.includes(environmentImport)) throw new Error("Terminal 4 environment import was not injected");
if (!prepared.includes("const environment = buildGround(scene);")) throw new Error("Terminal 4 environment was not connected to the scene");
if (!prepared.includes("camera, environment, tug")) throw new Error("Environment reference was not retained by simulator state");
if (prepared.includes("new THREE.PlaneGeometry(90, 140)")) throw new Error("Legacy flat ramp geometry remains in prepared trainer");

const banner = "// GENERATED by scripts/prepare-terminal4-runtime.mjs. Do not edit directly.\n";
fs.writeFileSync(outputPath, banner + prepared, "utf8");
console.log(`Prepared live Terminal 4 trainer: ${path.relative(root, outputPath)}`);
